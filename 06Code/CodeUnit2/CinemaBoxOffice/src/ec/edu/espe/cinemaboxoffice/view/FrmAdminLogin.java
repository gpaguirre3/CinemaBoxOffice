    private void txtUsernameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsernameKeyTyped
        char c = evt.getKeyChar();
        if ((c < 'a' || c > 'z') && (c < 'A') | c > 'Z' && (c < '0') | c > '9' && (c < '√ë')) {
            evt.consume();
        }
    }//GEN-LAST:event_txtUsernameKeyTyped

