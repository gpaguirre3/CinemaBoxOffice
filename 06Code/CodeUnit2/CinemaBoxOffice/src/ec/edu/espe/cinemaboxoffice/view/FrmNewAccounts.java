    public boolean verify(String id) throws IOException {
        LogInController getId = new LogInController();
        boolean takenId = false;
        takenId = getId.findId(id);
        if (takenId == true) {
            JOptionPane.showMessageDialog(null,
                    "That Id already exists",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            txtId.setText("");
            txtName.setText("");
            txtUsername.setText("");
            pwfPass.setText("");

        }
        return takenId;
    }

