    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
        NewAccountsController service = new NewAccountsController();
        PasswordEncryptor encryptor = new PasswordEncryptor();

        String id = txtId.getText();
        String name = txtName.getText();
        String username = txtUsername.getText();
        String password = pwfPass.getText();
        password = encryptor.encryptPassword(password);

        if (id.trim().isEmpty()) {
            JOptionPane.showMessageDialog(
                    null,
                    "id is required",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        try {
            boolean takenId = false;
            takenId = verify(id);
            if (takenId == true) {
                return;
            }
        } catch (IOException ex) {
            Logger.getLogger(FrmNewAccounts.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (name.trim().isEmpty()) {
            JOptionPane.showMessageDialog(
                    null,
                    "Name is required",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        if (username.trim().isEmpty()) {
            JOptionPane.showMessageDialog(
                    null,
                    "Username is required",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        if (password.trim().isEmpty()) {
            JOptionPane.showMessageDialog(
                    null,
                    "Password is required",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        service.addAccounts(id, name, username, password);

        JOptionPane.showMessageDialog(
                null,
                "Account has been registered",
                "Success",
                JOptionPane.INFORMATION_MESSAGE
        );

        txtId.setText("");
        txtName.setText("");
        txtUsername.setText("");
        pwfPass.setText("");

    }//GEN-LAST:event_btnSignUpActionPerformed

